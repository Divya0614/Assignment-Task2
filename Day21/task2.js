let fruits = ["Aplle", "Orange", "Banana","grapes","Watermelon"];
console.log(fruits);

//push
fruits.push("Mango");
console.log("Push:",fruits);

//pop
fruits.pop();
console.log("pop:",fruits);

//shift
fruits.shift();
console.log("shift:",fruits);

//unshift
fruits.unshift("Pomegranate");
console.log("unshift:",fruits);


